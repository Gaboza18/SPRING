package usual_case;

public class LgTV {

	public void turnOn() {
		System.out.println("LgTV -- 전원을 켭니다");
	}

	public void turnOff() {
		System.out.println("LgTV -- 전원을 끕니다");
	}

	public void soundUp() {
		System.out.println("LgTV -- 소리를 올립니다");
	}

	public void soundDown() {
		System.out.println("LgTV -- 소리를 내립니다");
	}
}
